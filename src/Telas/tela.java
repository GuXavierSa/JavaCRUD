    private void confirmarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarBtnActionPerformed
        // TODO add your handling code here:
        User objUser = new User();
       
        String nome = nomeTxt.getText();
        String email = emailTxt.getText(); 
        String senha = senhaPSW.getPassword().toString();
        String dia = diaSpi.getValue().toString();
        String mes = mesBox.getSelectedItem().toString();
        String ano = anoBox.getSelectedItem().toString();
        
        objUser.setNome(nome);
        objUser.setEmail(email);
        objUser.setSenha(senha);
        objUser.setDia(Integer.parseInt(dia));
        objUser.setMes(mes);
        objUser.setAno(Integer.parseInt(ano));
        
        new UserDao().cadastraUser(objUser);
        
        
    }//GEN-LAST:event_confirmarBtnActionPerformed

